<p class="py-4">Adenosine kinase is a ubiquitous enzyme that catalyzes the transfer of &gamma;-phosphate from ATP to 5'-hydroxyl of adenosine (AR), an important modulator of central nervous system functions and a signal molecule involved in hypoxia, inflammation, and nociception. Together with adenosine deaminase, adenosine kinase regulates intra- and extracellular cellular adenosine concentration. Inhibition of adenosine kinase results in a selective increase of local adenosine concentrations and reduced seizure susceptibility and nociception in vivo(3). ADK dysfunction is involved in several pathologies, including diabetes(13), epilepsy(6), and cancer. Consequently, ADK emerges as a rational therapeutic target for Drug Discovery, and adenosine-regulating drugs have been extensively tested (1) as new analgesic and anti-inflammatory agents(4) to treat schizophrenia(7) or to limit brain injury after an ischemic stroke(8). <br>
The X-ray crystallographic structure of human ADK has been described(9) and provides structural basis for rational design and optimisation of new ADK inhibitors. In addition, this enzyme is responsible for the phosphorylation and consequent clinical activity of several therapeutically useful nucleosides, including the antiviral drug ribavirin(10), immunosuppressive drug mizoribine(11), and anticancer C-nucleoside, tiazofurin(12).
